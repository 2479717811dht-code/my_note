`timescale 1ns / 1ps
module tb_MS_SR_FlipFlop;

    // 输入信号
    reg S;
    reg C;
    reg R;

    // 输出信号
    wire Q;
    wire Q_bar;

    // 例化主从SR触发器
    MS_SR_FlipFlop uut (
        .S(S),
        .C(C),
        .R(R),
        .Q(Q),
        .Q_bar(Q_bar)
    );

    // 时钟周期 40ns
    always #20 C = ~C;

    initial begin
        // 初始化
        C = 0;
        S = 0;
        R = 0;
        #30;

        // ----------------------
        // 1. 保持状态 S=0, R=0
        // ----------------------
        S = 0; R = 0;
        #40;

        // ----------------------
        // 2. 置1  S=1, R=0
        // ----------------------
        S = 1; R = 0;
        #40;

        // ----------------------
        // 3. 保持  S=0, R=0
        // ----------------------
        S = 0; R = 0;
        #40;

        // ----------------------
        // 4. 置0  S=0, R=1
        // ----------------------
        S = 0; R = 1;
        #40;

        // ----------------------
        // 5. 保持  S=0, R=0
        // ----------------------
        S = 0; R = 0;
        #40;

        // ----------------------
        // 6. 非法状态 S=1, R=1
        // ----------------------
        S = 1; R = 1;
        #40;

        // ----------------------
        // 7. 恢复正常 S=0, R=0
        // ----------------------
        S = 0; R = 0;
        #40;

        $stop;
    end

endmodule