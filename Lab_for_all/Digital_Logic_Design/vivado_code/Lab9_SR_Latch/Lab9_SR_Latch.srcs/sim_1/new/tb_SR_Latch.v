`timescale 1ns / 1ps

module tb_SR_Latch;
    reg R;
    reg S;
    wire Q;
    wire Q_bar;

    SR_Latch uut (
        .R(R),
        .S(S),
        .Q(Q),
        .Q_bar(Q_bar)
    );

    initial begin
        // 初始化：绝对不能 R=1 S=1！！！
        R = 0;
        S = 0;
        #50;

        // ==============================
        // 阶段1：置 0
        // ==============================
        R = 1;
        S = 0;
        #50;

        // ==============================
        // 阶段2：保持 0 （很长！）
        // ==============================
        R = 0;
        S = 0;
        #50;

        // ==============================
        // 阶段3：置 1
        // ==============================
        R = 0;
        S = 1;
        #50;

        // ==============================
        // 阶段4：保持 1 （很长！）
        // ==============================
        R = 0;
        S = 0;
        #50;

        // ==============================
        // 阶段5：再置 0
        // ==============================
        R = 1;
        S = 0;
        #50;

        // ==============================
        // 阶段6：再保持 0
        // ==============================
        R = 0;
        S = 0;
        #50;

        // ==============================
        // 阶段7：再置 1
        // ==============================
        R = 0;
        S = 1;
        #50;

        // ==============================
        // 阶段8：再保持 1
        // ==============================
        R = 0;
        S = 0;
        #50;

        $stop;
    end
endmodule